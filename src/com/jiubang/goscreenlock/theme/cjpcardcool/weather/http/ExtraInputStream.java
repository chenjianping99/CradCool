package com.jiubang.goscreenlock.theme.cjpcardcool.weather.http;

import java.io.InputStream;

/**
 *  
 * <br>类描述: 标识是否采用了压缩的输入流
 * 
 * @author  liuwenqin
 * @date  [2012-9-14]
 */
public class ExtraInputStream
{
	public boolean		mIsGzip;
	public InputStream	mInputStream;

	public ExtraInputStream(boolean isGzip, InputStream inputStream)
	{
		this.mIsGzip = isGzip;
		this.mInputStream = inputStream;
	}
}